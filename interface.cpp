void print_countdown(WINDOW *win) {
    wprintw(win, R"(
                                   _____
                                  |__  /
                                   /_ < 
                                 ___/ / 
                                /____/
    )");
    wrefresh(win);
    napms(1000);
    wclear(win);
    wprintw(win, R"(
                                   ___  
                                  |__ \
                                  __/ / 
                                 / __/  
                                /____/
    )");
    wrefresh(win);
    napms(1000);
    wclear(win);
    wprintw(win, R"(
                                   ___  
                                  <  /  
                                  / /   
                                 / /    
                                /_/
    )");
    wrefresh(win);
    napms(1000);
    wclear(win);
    wrefresh(win);
}

